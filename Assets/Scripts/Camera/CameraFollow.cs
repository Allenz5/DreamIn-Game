    public void SetTarget(GameObject t)
    {
        target = t.transform;
        offset = transform.position - target.transform.position;
    }

    /// <summary>
    /// 用于重新开始游戏时直接重置相机位置
    /// </summary>
    public void ResetPosition()
    {
        transform.position = startPosition;
    }
}
